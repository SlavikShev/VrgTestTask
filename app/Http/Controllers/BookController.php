    {
        Book::create($request->all());
        if ($request->file('bookCover')) {
            $image = $request->file('bookCover');
            // todo find better hash function
            $new_name = rand() . '.' . $image->getClientOriginalExtension();
            $image->move(public_path('book_covers'), $new_name);
        }
    }

